// Returns index of smallest element in array
// n - number of elements in array a
// a - array of integers
int findMinElement(int n, const int *a);

// Returns index of largest element in array
// n - number of elements in array a
// a - array of integers
int findMaxElement(int n, const int *a);

// Returns the count of occurences of two integers with different signs near each other in array a
// n - number of elements in array a
// a - array of integers
int countNeighbors(int n, const int *a);
